package in.lms.cca.dto;

public class AuditAgencyMobileDTO {
	private String auditAgencyMobileId;
	private String contactType;
	private String areaCode;
	private String mobile;
	private String status;
	private String created;
	private String updated;
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getUpdated() {
		return updated;
	}
	public void setUpdated(String updated) {
		this.updated = updated;
	}
	public String getAuditAgencyMobileId() {
		return auditAgencyMobileId;
	}
	public void setAuditAgencyMobileId(String auditAgencyMobileId) {
		this.auditAgencyMobileId = auditAgencyMobileId;
	}
	public String getContactType() {
		return contactType;
	}
	public void setContactType(String contactType) {
		this.contactType = contactType;
	}
	public String getAreaCode() {
		return areaCode;
	}
	public void setAreaCode(String areaCode) {
		this.areaCode = areaCode;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	
	
	

}
