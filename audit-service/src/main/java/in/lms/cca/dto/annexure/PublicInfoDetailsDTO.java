package in.lms.cca.dto.annexure;


public class PublicInfoDetailsDTO {

    private String publicInfoDetailsId;    
    private String description;
    private String webLink;
    
    
	public String getPublicInfoDetailsId() {
		return publicInfoDetailsId;
	}
	public void setPublicInfoDetailsId(String publicInfoDetailsId) {
		this.publicInfoDetailsId = publicInfoDetailsId;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getWebLink() {
		return webLink;
	}
	public void setWebLink(String webLink) {
		this.webLink = webLink;
	}
    
    
    
}
