package in.lms.cca.dto.annexure;

public class DownTimeDTO {

    private String downTimeId;
    private String reasonAndMeasuresTaken;
    private String downTimeHour;
    private String downTimeMinute;
    private String downTimeSecond;
    private String userName;
	public String getDownTimeId() {
		return downTimeId;
	}
	public void setDownTimeId(String downTimeId) {
		this.downTimeId = downTimeId;
	}
	public String getReasonAndMeasuresTaken() {
		return reasonAndMeasuresTaken;
	}
	public void setReasonAndMeasuresTaken(String reasonAndMeasuresTaken) {
		this.reasonAndMeasuresTaken = reasonAndMeasuresTaken;
	}
	public String getDownTimeHour() {
		return downTimeHour;
	}
	public void setDownTimeHour(String downTimeHour) {
		this.downTimeHour = downTimeHour;
	}
	public String getDownTimeMinute() {
		return downTimeMinute;
	}
	public void setDownTimeMinute(String downTimeMinute) {
		this.downTimeMinute = downTimeMinute;
	}
	public String getDownTimeSecond() {
		return downTimeSecond;
	}
	public void setDownTimeSecond(String downTimeSecond) {
		this.downTimeSecond = downTimeSecond;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
    
    
    
    
}
